
package proyecto;

import Registro.CuentaDeAhorro;
import javax.swing.*;
import java.awt.*;
import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;
import java.util.ArrayList;
import java.util.Date;

public class CuentaDeAhorroGUI extends JFrame {
    // Atributos de la clase
    private JTextField nombreField;
    private JTextField apellidoField;
    private JTextField cedulaField;
    private JTextField reporteAhorroField;
    private JTextField totalPlataField;
    private JTextArea historialArea;

    // Constructor de la clase
    public CuentaDeAhorroGUI() {
        // Configuración del JFrame
        super("Ingreso de datos de cuenta de ahorro");
        setSize(400, 500);
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);

        // Panel principal
        JPanel panel = new JPanel();
        panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));

        // Campos de texto para ingresar los datos de la cuenta
        nombreField = new JTextField(20);
        apellidoField = new JTextField(20);
        cedulaField = new JTextField(10);
        reporteAhorroField = new JTextField(10);
        totalPlataField = new JTextField(10);
        historialArea = new JTextArea(10, 30);

        // Botón para guardar los datos de la cuenta
        JButton guardarButton = new JButton("Guardar");
        guardarButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                // Obtener los datos ingresados por el usuario
                String nombre = nombreField.getText();  
                String apellido = apellidoField.getText();
                String cedula = cedulaField.getText();
                double reporteAhorro = Double.parseDouble(reporteAhorroField.getText());
                double totalPlata = Double.parseDouble(totalPlataField.getText());

                // Crear un objeto de la clase CuentaDeAhorro con los datos ingresados
                CuentaDeAhorro cuenta = new CuentaDeAhorro(nombre, apellido, cedula, new Date(), reporteAhorro, totalPlata, new ArrayList<>());

                // Mostrar los datos de la cuenta en la ventana
                historialArea.setText(cuenta.getHistorial().toString());
            }
        });

        // Panel para los campos de texto
        JPanel datosPanel = new JPanel(new GridLayout(6, 2));
        datosPanel.add(new JLabel("Nombre:"));
        datosPanel.add(nombreField);
        datosPanel.add(new JLabel("Apellido:"));
        datosPanel.add(apellidoField);
        datosPanel.add(new JLabel("Cédula:"));
        datosPanel.add(cedulaField);
        datosPanel.add(new JLabel("Reporte de ahorro:"));
        datosPanel.add(reporteAhorroField);
        datosPanel.add(new JLabel("Total en plata:"));
        datosPanel.add(totalPlataField);
        datosPanel.add(new JLabel("Historial:"));
        datosPanel.add(new JScrollPane(historialArea));

        // Añadir los componentes al panel principal
        panel.add(datosPanel);
        panel.add(guardarButton);

        // Mostrar la ventana
        setContentPane(panel);
        setVisible(true);
    }

    // Método main para ejecutar la aplicación
    public static void main(String[] args) {
        CuentaDeAhorroGUI gui = new CuentaDeAhorroGUI();
    }
}

